    updated_at timestamp(6) with time zone NOT NULL,
    vulnerable_configuration boolean,
    vulnerable_version_present boolean,
    vulnerable_version_range character varying(200),
    tenant_id uuid NOT NULL,
    vulnerability_id uuid NOT NULL,
    CONSTRAINT applicability_assessments_confidence_level_check CHECK (((confidence_level)::text = ANY ((ARRAY['HIGH'::character varying, 'MEDIUM'::character varying, 'LOW'::character varying])::text[]))),
