-- Postgres-safe core schema baseline for empty databases.
-- This migration is intentionally idempotent so it can also run on schemas that
-- already have the tables created by Hibernate or earlier incremental Flyway migrations.
-- Note: vulnerability_targets is defined manually here because it was still absent
-- from the working bootstrap schema when this baseline was generated.
-- Note: vulnerability_config_expr and vulnerability_intel_observations are excluded here
-- because they are already fully owned by V1007 and V1008.

